// Author-time bundler: Topola (CJS + deps) -> single self-contained IIFE global `topola`.
// Produces Koreny/wwwroot/lib/topola/topola.bundle.js. Run with: npm ci && npm run build
// (see README.md). The app never runs this — it uses the committed bundle.
import { build } from "esbuild";
import { createRequire } from "node:module";
import { createHash } from "node:crypto";
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const require = createRequire(import.meta.url);
const outFile = resolve(here, "../../Koreny/wwwroot/lib/topola/topola.bundle.js");
mkdirSync(dirname(outFile), { recursive: true });

// Entry re-exports topola's public API onto the IIFE global `topola`.
const entry = resolve(here, "entry.js");
writeFileSync(entry, 'export * from "topola";\n');

await build({
  entryPoints: [entry],
  bundle: true,
  format: "iife",
  globalName: "topola",
  platform: "browser",
  target: ["es2019"],
  outfile: outFile,
  legalComments: "none",
  logLevel: "info",
});

// Pin resolved dependency versions into a banner + report the bundle hash.
const topolaVer = require("topola/package.json").version;
const bundle = readFileSync(outFile, "utf8");
const banner = `/* Vendored Topola bundle — topola@${topolaVer}, self-contained IIFE (global "topola").
   Generated by tools/bundle-topola (esbuild). Do not edit by hand. */\n`;
writeFileSync(outFile, banner + bundle);

const hash = createHash("sha256").update(readFileSync(outFile)).digest("hex");
console.log(`\ntopola@${topolaVer} -> ${outFile}`);
console.log(`sha256: ${hash}`);
console.log(`bytes:  ${readFileSync(outFile).length}`);
